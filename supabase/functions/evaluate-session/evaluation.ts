import { z } from "npm:zod@3.23.8";

import { HttpError } from "../_shared/errors.ts";

export const EVAL_PROMPT_VERSION = "2026-03-21.v1";

const severityRank: Record<"low" | "medium" | "high", number> = {
  high: 0,
  medium: 1,
  low: 2,
};

export const feedbackItemSchema = z.object({
  category: z.string().min(1),
  severity: z.enum(["low", "medium", "high"]),
  quote: z.string().optional(),
  explanation: z.string().min(1),
  suggestion: z.string().min(1),
  mistake_key: z.string().min(1).optional(),
}).strict();

export const evaluationSchema = z.object({
  summary: z.string().min(1),
  score: z.number().finite(),
  confidence: z.number().min(0).max(1),
  feedback_items: z.array(feedbackItemSchema),
}).strict();

const rubricCategorySchema = z.object({
  key: z.string().min(1),
  title: z.string().optional(),
  description: z.string().optional(),
  weight: z.number().min(0).max(1).optional(),
  scoring_guidelines: z.array(z.string()).optional(),
  common_mistakes: z.array(z.string()).optional(),
}).passthrough();

const rubricSchema = z.object({
  title: z.string().optional(),
  description: z.string().optional(),
  categories: z.array(rubricCategorySchema).optional(),
}).passthrough();

export type Evaluation = z.infer<typeof evaluationSchema>;

export const clampScore = (score: number): number => Math.max(0, Math.min(100, score));

export const normalizeRubric = (rubric: unknown): Record<string, unknown> => {
  const parsed = rubricSchema.safeParse(rubric);
  if (!parsed.success) {
    return {
      title: "Session rubric",
      description: "Default rubric generated by normalization.",
      categories: [],
    };
  }

  const categories = (parsed.data.categories ?? []).map((category) => ({
    key: category.key,
    title: category.title ?? category.key,
    description: category.description ?? "",
    weight: category.weight ?? 0,
    scoring_guidelines: category.scoring_guidelines ?? [],
    common_mistakes: category.common_mistakes ?? [],
  }));

  return {
    ...parsed.data,
    title: parsed.data.title ?? "Session rubric",
    description: parsed.data.description ?? "",
    categories,
  };
};

export const mapTopIssues = (feedbackItems: Evaluation["feedback_items"], limit = 3): string[] => {
  return [...feedbackItems]
    .sort((a, b) => {
      const severityDiff = severityRank[a.severity] - severityRank[b.severity];
      if (severityDiff !== 0) return severityDiff;

      const keyA = a.mistake_key ?? `${a.category}:${a.explanation}`;
      const keyB = b.mistake_key ?? `${b.category}:${b.explanation}`;
      return keyA.localeCompare(keyB);
    })
    .slice(0, limit)
    .map((item) => item.mistake_key ?? `${item.category}:${item.explanation}`);
};

export const evaluateWithSchemaRetry = async (
  evaluator: () => Promise<unknown>,
): Promise<Evaluation & { top_issues: string[] }> => {
  let lastError: z.ZodError | null = null;

  for (let attempt = 0; attempt < 2; attempt++) {
    const raw = await evaluator();
    const parsed = evaluationSchema.safeParse(raw);
    if (!parsed.success) {
      lastError = parsed.error;
      continue;
    }

    const normalized: Evaluation = {
      ...parsed.data,
      score: clampScore(parsed.data.score),
    };

    return {
      ...normalized,
      top_issues: mapTopIssues(normalized.feedback_items),
    };
  }

  console.error("evaluation_invalid_schema", { issues: lastError?.issues });
  throw new HttpError(502, "evaluation_invalid_schema", "Evaluation output failed schema validation.");
};
